#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	while (lst)
	{
		(*f)(lst->content);
		lst = lst->next;
	}
}

// La función aplica la función *f a cada nodo de
// la lista lst, la cual se va iterando.